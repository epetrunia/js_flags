/**
 * 1. дан массив, нужно проверить, есть ли в нем элемент со значением 'c' или нет.
 * Если есть - вывести 'есть', если нет - вывести 'нет'.
 */

console.log(
  '1. дан массив, нужно проверить, есть ли в нем элемент со значением \'c\' или нет. Если есть - вывести \'есть\', если нет - вывести \'нет\'.' );

const array = ['a', 2, 'c', true];
let isElement = false;

for (let i = 0; i < array.length; i++) {
  if (array[i] === 'c') {
    isElement = true;
    break; // stop cycle
  }
}

console.log( `В массиве [${array}] ${isElement
                                     ? 'есть'
                                     : 'нет'}` );

/**
 * 2. необходимо создать функцию hasElem,
 * которая параметром будет принимать массив и возвращать true,
 * если 'c' есть в массиве, и false - если нет
 *
 */

/**
 *
 * @param {Array} arr
 * @returns {boolean}
 */
function hasElem(arr) {
  for (let i = 0; i < arr.length; i++) {
    if (arr[i] === 'c') {
      return true;
    }
  }
  return false;
}

const array2 = ['a', 2, 'c', true];
console.log( `${hasElem( array2 )} in array2 [${array2}]` );

/**
 * Дан массив с числами.
 * Проверьте, что в этом массиве есть число 5.
 * Если есть - выведите 'да', а если нет - выведите 'нет'
 *
 */

/**
 *
 * @param {Array<number>} arr
 * @return {boolean}
 */
function hasNum(arr) {
  for (let i = 0; i < arr.length; i++) {
    if (arr[i] === 5) {
      return true;
    }
  }
  return false;
}

const array3 = [1, 2, 3, 4, 5, 6, 5];

console.log( `${hasNum( array3 )
                ? 'да'
                : 'нет'}, в массиве [${array3}]` );

